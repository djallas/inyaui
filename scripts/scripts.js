// switch nav bar
function toggleSidebar() {
   var m = document.getElementById("sidebar");
   var l = document.getElementById("layer");
   m.style.display = "block";
   l.style.display = "block";
   
}
// close
function closeSidebar() {
   var m = document.getElementById("sidebar");
   var l = document.getElementById("layer");
   m.style.display = "none";
   l.style.display = "none";
}
